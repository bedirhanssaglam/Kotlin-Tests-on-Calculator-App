package com.example.calculator.enums

/**
 * Enum representing different types of actions performed in the calculator app.
 * These actions include entering a digit, decimal point, or an operator.
 */
enum class ActionType {
    /** Represents the action of entering a digit. */
    DIGIT,

    /** Represents the action of entering a decimal point. */
    DECIMAL_POINT,

    /** Represents the action of entering an operator. */
    OPERATOR
}